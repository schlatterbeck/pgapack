#define NOBJ  3
#define DIM  12
#define K (DIM - NOBJ + 1)

static double g (double *x)
{
    int i;
    double s = 0;
    for (i=0; i<K; i++) {
        s += pow (x [i] - 0.5, 2);
    }
    return s;
}

static void f (double *x, double *y)
{
    int i, j;
    double gv = g (x + DIM - K);
    double s1 = 0, s2 = 0;
    double m = 0;
    double r2 = (NOBJ == 3) ? 0.4 * 0.4 : 0.5 + 0.5;
    double sqm = sqrt (NOBJ);
    for (i=0; i<NOBJ; i++) {
        double p = 1;
        for (j=0; j<NOBJ-i-1; j++) {
            p *= cos (x [j] * M_PI / 2.0);
        }
        if (j < NOBJ-1) {
            p *= sin (x [j] * M_PI / 2.0);
        }
        y [i] = p * (1 + gv);
    }
    for (i=0; i<NOBJ; i++) {
        double d;
        s2 += pow (y [i] - 1.0 / sqm, 2);
        s1 = 0;
        for (j=0; j<NOBJ; j++) {
            if (i != j) {
                s1 += pow (y [j], 2);
            }
        }
        d = pow (y [i] - 1, 2) + s1 - r2;
        if (i == 0 || d < m) {
            m = d;
        }
    }
    s2 -= r2;
    if (m < s2) {
        y [NOBJ] = m;
    } else {
        y [NOBJ] = s2;
    }
}

